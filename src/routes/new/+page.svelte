<script lang="ts">
	import { noteStore } from '$lib/stores';
	import { InputChip } from '@skeletonlabs/skeleton';

	let tags: string[] = [];
	let content: string;

	function createNote() {
		noteStore.update((notes) => [
			...notes,
			{
				id: crypto.randomUUID(),
				content,
				tags
			}
		]);
		content = '';
		tags = [];
	}
</script>

<div class="container h-full mx-auto gap-8 flex flex-col">
	<form action="" class="card p-4 flex flex-col gap-3">
		<h2>New Note</h2>
		<textarea
			bind:value={content}
			class="textarea"
			rows="4"
			placeholder="Enter some long form content."
		/>
		<InputChip bind:value={tags} name="tags" placeholder="Enter any value..." />
		<button type="button" on:click={createNote} class="btn variant-ghost-primary self-end"
			>Create Note</button
		>
	</form>
</div>
